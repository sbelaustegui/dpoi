#!/usr/bin/env python3
import requests
from bs4 import BeautifulSoup
import json


def get_jsonld(url):
    """This function prints the json-ld of a given url"""
    response = requests.get(url, timeout=20)
    content = BeautifulSoup(response.content, "html.parser")
    json_ld = content.find('script', attrs={"type": "application/ld+json"})
    if json_ld:
        json_ld = json_ld.contents[0]
        print('-------------------- ' + url + ' --------------------------')
        print(json.dumps(json.loads(json_ld), sort_keys=True, indent=4))
    else:
        print('Json-ld Not Found for ' + url + ' !')


